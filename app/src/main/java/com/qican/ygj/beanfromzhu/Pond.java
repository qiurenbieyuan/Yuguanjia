package com.qican.ygj.beanfromzhu;

public class Pond {
	private String pondId;
	private String userId;
	private String pondName;
	private String pondLocation;
	private String pondImageUrl;
	private String pondDescrible;

	public String getPondId() {
		return pondId;
	}

	public void setPondId(String pondId) {
		this.pondId = pondId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getPondName() {
		return pondName;
	}

	public void setPondName(String pondName) {
		this.pondName = pondName;
	}

	public String getPondLocation() {
		return pondLocation;
	}

	public void setPondLocation(String pondLocation) {
		this.pondLocation = pondLocation;
	}

	public String getPondImageUrl() {
		return pondImageUrl;
	}

	public void setPondImageUrl(String pondImageUrl) {
		this.pondImageUrl = pondImageUrl;
	}

	public String getPondDescrible() {
		return pondDescrible;
	}

	public void setPondDescrible(String pondDescrible) {
		this.pondDescrible = pondDescrible;
	}

}
